func valid(sum string) bool {
	ep := envar["BASE_URL"] + "/check_releases/" + sum
	//log.Println("wrapper: release check url:", ep)
	resp, err := http.Get(ep)
	if err != nil {
		log.Panic(err)
	}
	//log.Println("wrapper: valid: response status:", resp.Status)

	switch resp.StatusCode {
	case 200:
		return true
	case 404:
		return false
	default:
		log.Panic("wrapper: valid: got unexpected status ", resp.Status)
	}
	return false
}

// Device contains information that need to be posted to device endpoint
type Device struct {
	Mac  string `json:"mac_address_hash,omitempty"`
	Zone string `json:"timezone,omitempty"`
}

func postDevice() error {
	//Mac addresses are generally 6 bytes long
	sum := make([]byte, 6)
	var hash string
	interfaces, err := net.Interfaces()

	// If we cant get interfaces on the device we send empty hash
	if err != nil {
		hash = ""
	}

	if err == nil {
		for _, i := range interfaces {
			if bytes.Compare(i.HardwareAddr, nil) != 0 {
				fmt.Println([]byte(i.HardwareAddr))
				for h, k := range []byte(i.HardwareAddr) {
					sum[h] += k
				}
			}
		}
		hash = fmt.Sprintf("%x", md5.Sum(sum))
	}

	zone, _ := time.Now().Zone()
	d := Device{
		Mac:  hash,
		Zone: zone,
	}

	b, err := json.Marshal(d)
	url := envar["BASE_URL"] + "/devices"

	client := &http.Client{
		Timeout: 5 * time.Second,
	}
	req, err := http.NewRequest("POST", url, bytes.NewReader(b))
	if err != nil {
		return err
	}

	req.Header.Add("content-type", "application/json")
	req.Header.Add("apikey", envar["API_KEY"])

	resp, err := client.Do(req)
	if err != nil {
		return err
	}
	log.Print(resp.Status)

	//this is assuming device endpoint have the same response codes as release endpoint
	switch resp.StatusCode {
	case 200:
		log.Println("not created")
	case 201: // created
		log.Printf("Device object created")
	case 502: // bad gateway
		log.Printf("Bad Gateway")
	default:

	}
	// If we get to this point no matter what response code is this function will return nil
	return err

}

var envar map[string]string

func env() {
	envar = make(map[string]string)
	keys := []string{
		"BASE_URL",
		"BROKERS",
		"PROF_TOPIC",
		"EVENT_TOPIC",
		"CA",
		"CERT",
		"PRIVATE_KEY",
	}

	prefix := "AUKLET_"
	ok := true
	for _, k := range keys {
		v := os.Getenv(prefix + k)
		if v == "" {
			ok = false
			log.Printf("empty envar %v\n", prefix+k)
		} else {
			envar[k] = v
		}
	}
	if !ok {
		log.Fatal("incomplete configuration")
	}
}

var currentIP string

func main() {
	logger := os.Stdout
	log.SetOutput(logger)

	env()

	args := os.Args
	if len(args) < 2 {
		usage()
	}
	cmd := exec.Command(args[1], args[2:]...)

	cksum = checksum(cmd.Path)
	if !valid(cksum) {
		//log.Fatal("invalid checksum: ", cksum)
		log.Print("invalid checksum: ", cksum)
	}

	conn, err := net.Dial("udp", "8.8.8.8:80")
	defer conn.Close()
	localAddr := conn.LocalAddr().(*net.UDPAddr)
	currentIP = localAddr.IP.String()

	//todo: What needs to be done if the device object doesnt get posted
	devicePosted := postDevice()
	if devicePosted != nil {
		log.Print("Did not make a post request")
	}

	obj := make(chan Object)

	wprod, err := produce(obj)
	check(err)
	defer wprod()

	wrelay, err := relay(obj)
	check(err)
	defer wrelay()

	lc, err := logs(logger)
	check(err)
	defer lc()

	run(obj, cmd)
	close(obj)
}
